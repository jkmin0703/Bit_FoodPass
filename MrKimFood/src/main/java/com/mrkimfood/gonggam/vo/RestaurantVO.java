package com.mrkimfood.gonggam.vo;

import java.io.Serializable;

public class RestaurantVO implements Serializable{
	
	private Integer restaurant_ID;
	private String restaurant_name;
	private String address;
	private String restaurant_type;
	private String openingTime;
	private String closingTime;
	private String res_phone;
	private String latitude;
	private String longitude;
	private Integer member_ID;
	private Character is_manage;
	
	public Integer getRestaurant_ID() {
		return restaurant_ID;
	}
	public void setRestaurant_ID(Integer restaurant_ID) {
		this.restaurant_ID = restaurant_ID;
	}
	public String getRestaurant_name() {
		return restaurant_name;
	}
	public void setRestaurant_name(String restaurant_name) {
		this.restaurant_name = restaurant_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getRestaurant_type() {
		return restaurant_type;
	}
	public void setRestaurant_type(String restaurant_type) {
		this.restaurant_type = restaurant_type;
	}
	public String getOpeningTime() {
		return openingTime;
	}
	public void setOpeningTime(String openingTime) {
		this.openingTime = openingTime;
	}
	public String getClosingTime() {
		return closingTime;
	}
	public void setClosingTime(String closingTime) {
		this.closingTime = closingTime;
	}
	public String getRes_phone() {
		return res_phone;
	}
	public void setRes_phone(String res_phone) {
		this.res_phone = res_phone;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public Integer getMember_ID() {
		return member_ID;
	}
	public void setMember_ID(Integer member_ID) {
		this.member_ID = member_ID;
	}
	public Character getIs_manage() {
		return is_manage;
	}
	public void setIs_manage(Character is_manage) {
		this.is_manage = is_manage;
	}
	
	
}